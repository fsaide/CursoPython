import math #importamos modulo de matematica

print(round(1.3)) #redondea al numero mas cercano
print(round(1.6))
print(abs(-44)) #valor absoluto

print(math.ceil(1.1111)) #funcion techo
print(math.floor(1.999)) #funcion piso
print(math.isnan(23)) #nos dice si es un numero real o no
print(math.pow(10,3)) #potencia (10^3 en este caso)
print(math.sqrt(9)) #raiz cuadrada
